class CbinsightSpider(scrapy.Spider):
    name = 'crunchbase'
    allowed_domains = ['www.crunchbase.com']
    start_urls = [
        'https://news.crunchbase.com/unicorn-company-list/'
    ]

    #Rules

    def parse(self, response):
        all_companies = response.xpath("//td/a/@href")
        for company in all_companies:
            # yield scrapy.Request(company.get(), callback=self.parse_company, headers={'User-Agent': random.choice(user_agent_list)})
            yield scrapy.Request(company.get(), callback=self.parse_company, headers=header)
            # yield scrapy.Request(company.get(), callback=self.parse_company)
        
    def parse_company(self, response):
        if response.status != requests.codes.ok:
            return
        title = response.xpath("//h1[contains(@class,'profile-name')]/text()").get()
        if title == None:
            print(response.url)
        yield {
            'Title': title
        }
